public class A1_2ExemploCharEBoolean {
    public static void main(String[] args) {
        // Tipo caractere
        char letraA = 'A';            // Tipo char, representa um único caractere
        char simboloExclamacao = '!'; // Outro exemplo de char com um símbolo

        // Tipo booleano
        boolean verdadeiro = true;   // Tipo boolean, valores true ou false
        boolean falso = false;       // Outro exemplo com o valor false

        // Exibindo os valores das variáveis char
        System.out.println("Caractere letra A: " + letraA);
        System.out.println("Caractere símbolo de exclamação: " + simboloExclamacao);

        // Exibindo os valores das variáveis boolean
        System.out.println("Valor booleano verdadeiro: " + verdadeiro);
        System.out.println("Valor booleano falso: " + falso);

        // Usando o operador ternário no println
        System.out.println("É verdadeiro? " + (verdadeiro ? "sim" : "não"));
        System.out.println("É falso? " + (falso ? "sim" : "não"));
    }
}


